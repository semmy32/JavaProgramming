public class ArithmeticOperators {
    public static void main(String[] args) {
int a= 100;
double b = a/6;
        System.out.println(b);  // 16.0

        double c = (double)a/6;
        System.out.println(c);  //16.666
        System.out.println(100D);




    }
}
